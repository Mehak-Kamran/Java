//implicit casting and explicit casting

public class casting{

public static void main(String args[]){
    System.out.println("Casting");
    //implicit
    int weight=45;
    float weight1=weight;
    System.out.println(weight1);

    //explicit
    float age=23.6F;
    int age2=(int)age;
    System.out.println(age2);

    //constant
    final float PI=3.142F;
    System.out.println(PI);//WE USE CAPITAL LETTERS FOR DEFINING CONSTANT IN JAVA
    // pi=2;
    // System.out.println(pi);//thus value is changed
    





}
}

